package com.channelsoft.ems.po;


/**
 * 老人健康信息PO(普通对象)类
 * @author lizhu
 *
 */
public class OldManHealthyPo {
	//老人健康信息 主键 自增长
	private int fID;
	//老人信息主键  外键
	private String fOldManID;
	//老人姓名
	private String fOldManName;
	//老人健康情况
	private String fHealthyCondition;
	//老人病史
	private String fMedicalHistory;
	//老人饮食特点
	private String fDietTrait;
	//老人嗜好
	private String fHobby;
	//老人注意事项
	private String fAttention;
	//养老院id
	private String fNursingHomeID;
	//创建人id
	private String fCreatorID;
	//创建时间
	private String fCreateTime;
	
	
	public int getfID() {
		return fID;
	}

	public void setfID(int fID) {
		this.fID = fID;
	}

	public String getfOldManID() {
		return fOldManID;
	}

	public void setfOldManID(String fOldManID) {
		this.fOldManID = fOldManID;
	}

	public String getfHealthyCondition() {
		return fHealthyCondition;
	}

	public void setfHealthyCondition(String fHealthyCondition) {
		this.fHealthyCondition = fHealthyCondition;
	}

	public String getfMedicalHistory() {
		return fMedicalHistory;
	}

	public void setfMedicalHistory(String fMedicalHistory) {
		this.fMedicalHistory = fMedicalHistory;
	}

	public String getfDietTrait() {
		return fDietTrait;
	}

	public void setfDietTrait(String fDietTrait) {
		this.fDietTrait = fDietTrait;
	}

	public String getfHobby() {
		return fHobby;
	}

	public void setfHobby(String fHobby) {
		this.fHobby = fHobby;
	}

	public String getfAttention() {
		return fAttention;
	}

	public void setfAttention(String fAttention) {
		this.fAttention = fAttention;
	}

	public String getfNursingHomeID() {
		return fNursingHomeID;
	}

	public void setfNursingHomeID(String fNursingHomeID) {
		this.fNursingHomeID = fNursingHomeID;
	}

	public String getfCreatorID() {
		return fCreatorID;
	}

	public void setfCreatorID(String fCreatorID) {
		this.fCreatorID = fCreatorID;
	}

	public String getfCreateTime() {
		return fCreateTime;
	}

	public void setfCreateTime(String fCreateTime) {
		this.fCreateTime = fCreateTime;
	}
	
	public String getfOldManName() {
		return fOldManName;
	}

	public void setfOldManName(String fOldManName) {
		this.fOldManName = fOldManName;
	}

	public OldManHealthyPo(){
		
	}
	
	/**
	 * @param fID
	 * @param fOldManID
	 * @param fHealthyCondition
	 * @param fMedicalHistory
	 * @param fDietTrait
	 * @param fHobby
	 * @param fAttention
	 * @param fNursingHomeID
	 * @param fCreatorID
	 * @param fCreateTime
	 */
	public OldManHealthyPo(int fID,String fOldManID,String fOldManName,String fHealthyCondition,String fMedicalHistory,String fDietTrait,String fHobby,String fAttention,String fNursingHomeID,String fCreatorID,String fCreateTime){
		this.fID=fID;
		this.fOldManID=fOldManID;
		this.fOldManName=fOldManName;
		this.fHealthyCondition=fHealthyCondition;
		this.fMedicalHistory=fMedicalHistory;
		this.fDietTrait=fDietTrait;
		this.fHobby=fHobby;
		this.fAttention=fAttention;
		this.fNursingHomeID=fNursingHomeID;
		this.fCreatorID=fCreatorID;
		this.fCreateTime=fCreateTime;
	}
	@Override
	public String toString() {
		
		return "OldManHealthy{"
				+ "fID=" + fID +
                ", fOldManID=" + fOldManID +
                ", fOldManName=" + fOldManName +
                ", fHealthyCondition='" + fHealthyCondition + '\'' +
                ", fMedicalHistory='" + fMedicalHistory + '\'' +
                ", fDietTrait='" + fDietTrait + '\'' +
                ", fHobby='" + fHobby + '\'' +
                ", fAttention='" + fAttention + '\'' +
                ", fNursingHomeID='" + fNursingHomeID + '\'' +
                ", fCreatorID='" + fCreatorID + '\'' +
                ", fCreateTime='" + fCreateTime  + '\'' +
                '}';
	}
}
