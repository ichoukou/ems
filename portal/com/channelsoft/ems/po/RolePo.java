package com.channelsoft.ems.po;

import java.io.Serializable;

/**
 * Created by Administrator on 2016/12/1.
 */
public class RolePo implements Serializable{
    private Integer page;
    private Integer pageSize;
    private String id;
    private String name;
    private String attr;
    private String oldHouse;
    private String oldHouseId;
    private String state;

    @Override
    public String toString() {
        return "RolePo{" +
                "page=" + page +
                ", pageSize=" + pageSize +
                ", id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", attr='" + attr + '\'' +
                ", oldHouse='" + oldHouse + '\'' +
                ", oldHouseId='" + oldHouseId + '\'' +
                ", state='" + state + '\'' +
                '}';
    }

    public RolePo() {
    }

    public RolePo(Integer page, Integer pageSize, String id, String name, String attr, String oldHouse, String oldHouseId, String state) {
        this.page = page;
        this.pageSize = pageSize;
        this.id = id;
        this.name = name;
        this.attr = attr;
        this.oldHouse = oldHouse;
        this.oldHouseId = oldHouseId;
        this.state = state;
    }

    public Integer getPage() {
        return page;
    }

    public void setPage(Integer page) {
        this.page = page;
    }

    public Integer getPageSize() {
        return pageSize;
    }

    public void setPageSize(Integer pageSize) {
        this.pageSize = pageSize;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAttr() {
        return attr;
    }

    public void setAttr(String attr) {
        this.attr = attr;
    }

    public String getOldHouse() {
        return oldHouse;
    }

    public void setOldHouse(String oldHouse) {
        this.oldHouse = oldHouse;
    }

    public String getOldHouseId() {
        return oldHouseId;
    }

    public void setOldHouseId(String oldHouseId) {
        this.oldHouseId = oldHouseId;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }
}
