public class PublicServicePlanPo {
    private String fID;
    private String fstatus;
    private String fRoomName;

    public String getfID() {
        return fID;
    }

    public void setfID(String fID) {
        this.fID = fID;
    }

    public String getFstatus() {
        return fstatus;
    }

    public void setFstatus(String fstatus) {
        this.fstatus = fstatus;
    }

    public String getfRoomName() {
        return fRoomName;
    }

    public void setfRoomName(String fRoomName) {
        this.fRoomName = fRoomName;
    }

    public PublicServicePlanPo(String fID, String fstatus, String fRoomName) {
        this.fID = fID;
        this.fstatus = fstatus;
        this.fRoomName = fRoomName;
    }

    public PublicServicePlanPo() {
    }

    @Override
    public String toString() {
        return "PublicServicePlanPo{" +
                "fID='" + fID + '\'' +
                ", fstatus='" + fstatus + '\'' +
                ", fRoomName='" + fRoomName + '\'' +
                '}';
    }
}
