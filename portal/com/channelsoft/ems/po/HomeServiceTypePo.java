public class HomeServiceTypePo {
    private String id;
    private String nursing_homeID;
    private String number;
    private String status;
    private String serviceTypeName;

    public HomeServiceTypePo() {
        super();
    }

    public HomeServiceTypePo(String id, String nursing_homeID, String number, String status, String serviceTypeName) {
        this.id = id;
        this.nursing_homeID = nursing_homeID;
        this.number = number;
        this.status = status;
        this.serviceTypeName = serviceTypeName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNursing_homeID() {
        return nursing_homeID;
    }

    public void setNursing_homeID(String nursing_homeID) {
        this.nursing_homeID = nursing_homeID;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getServiceTypeName() {
        return serviceTypeName;
    }

    public void setServiceTypeName(String serviceTypeName) {
        this.serviceTypeName = serviceTypeName;
    }

    @Override
    public String toString() {
        return "HomeServiceTypePo{" +
                "id='" + id + '\'' +
                ", nursing_homeID='" + nursing_homeID + '\'' +
                ", number='" + number + '\'' +
                ", status='" + status + '\'' +
                ", serviceTypeName='" + serviceTypeName + '\'' +
                '}';
    }
}
