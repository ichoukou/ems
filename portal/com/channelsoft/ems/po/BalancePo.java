public class BalancePo {
	private String fId;//primary key
	private String fNursinghomeID;//养老院id
	private String fFundaccountID;//账户id
	private String fPeriod;//期间年
	private String fMoun;//期间月
	private String fAmount;//期初金额
	private String fOutamount;//支出
	private String fInamount;//收入
	private String fBalance;//余额

	public BalancePo(){

	}
	public BalancePo(String fId,String fNursinghomeID,String fFundaccountID,String fPeriod,
					 String fMoun,String fAmount,String fOutamount,String fInamount,String fBalance){
		this.fId=fId;
		this.fNursinghomeID=fNursinghomeID;
		this.fFundaccountID=fFundaccountID;
		this.fPeriod=fPeriod;
		this.fMoun=fMoun;
		this.fAmount=fAmount;
		this.fOutamount=fOutamount;
		this.fInamount=fInamount;
		this.fBalance=fBalance;
	}
	public String toString(){
		return "BalancePo["
				+ "fId='"+fId+"',"
				+ "fNursinghomeID='"+fNursinghomeID+"',"
				+ "fFundaccountID='"+fFundaccountID+"',"
				+ "fPeriod='"+fPeriod+"',"
				+ "fMoun='"+fMoun+"',"
				+ "fAmount='"+fAmount+"',"
				+ "fOutamount='"+fOutamount+"',"
				+ "fInamount='"+fInamount+"',"
				+ "fBalance='"+fBalance+"']";
	}
	public String getfId() {
		return fId;
	}

	public void setfId(String fId) {
		this.fId = fId;
	}

	public String getfNursinghomeID() {
		return fNursinghomeID;
	}

	public void setfNursinghomeID(String fNursinghomeID) {
		this.fNursinghomeID = fNursinghomeID;
	}

	public String getfFundaccountID() {
		return fFundaccountID;
	}

	public void setfFundaccountID(String fFundaccountID) {
		this.fFundaccountID = fFundaccountID;
	}

	public String getfPeriod() {
		return fPeriod;
	}

	public void setfPeriod(String fPeriod) {
		this.fPeriod = fPeriod;
	}

	public String getfMoun() {
		return fMoun;
	}

	public void setfMoun(String fMoun) {
		this.fMoun = fMoun;
	}

	public String getfAmount() {
		return fAmount;
	}

	public void setfAmount(String fAmount) {
		this.fAmount = fAmount;
	}

	public String getfOutamount() {
		return fOutamount;
	}

	public void setfOutamount(String fOutamount) {
		this.fOutamount = fOutamount;
	}

	public String getfInamount() {
		return fInamount;
	}

	public void setfInamount(String fInamount) {
		this.fInamount = fInamount;
	}

	public String getfBalance() {
		return fBalance;
	}

	public void setfBalance(String fBalance) {
		this.fBalance = fBalance;
	}



}
